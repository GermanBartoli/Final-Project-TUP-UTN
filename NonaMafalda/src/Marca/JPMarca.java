    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
        if (validar()) {

            int idMarca;

            if (banderaAgregarEditar) {
                idMarca = -1;
            } else {
                idMarca = jLMarcas.getSelectedValue().getId_Marca();
            }

            String nombre = jTFMarca.getText();

            CMarca marca = new CMarca(idMarca, nombre, true);

              if (banderaAgregarEditar) {
                if (gestorBDMarca.ExisteNombreMarcaAgregar(marca)) {
                    JOptionPane.showMessageDialog(
                            this,
                            "Â¡Marca Repetida!",
                            "Advertencia - Nona Mafalda",
                            JOptionPane.WARNING_MESSAGE);
                    jTFMarca.requestFocus();
                    return;
                }
            } else {
                if (gestorBDMarca.ExisteNombreMarcaEditar(marca)) {
                    JOptionPane.showMessageDialog(
                            this,
                            "Â¡Marca Repetida!",
                            "Advertencia - Nona Mafalda",
                            JOptionPane.WARNING_MESSAGE);
                    jTFMarca.requestFocus();
                    return;
                }
            }

            boolean exito = false;

            if (idMarca == -1) {
                exito = gestorBDMarca.AgregarMarca(marca);
            } else {
                try {
                    exito = gestorBDMarca.ModificarMarca(marca);
                } catch (SQLException ex) {
                    Logger.getLogger(JPMarca.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (exito) {
                JOptionPane.showMessageDialog(
                        this,
                        "La Marca se guardÃ³ Correctamente",
                        "Exito - Nona Mafalda",
                        JOptionPane.INFORMATION_MESSAGE);

                CargarLista();

                for (int i = 0; i < jLMarcas.getModel().getSize(); i++) {
                    if (jLMarcas.getModel().getElementAt(i).getNombre().equals(nombre)) {
                        jLMarcas.setSelectedIndex(i);
                    }
                }
            }
        }
    }//GEN-LAST:event_jBGuardarActionPerformed

