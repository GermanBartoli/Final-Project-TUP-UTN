    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
        if (validar()) {

            int idFormaPago;

            if (banderaAgregarEditar) {
                idFormaPago = -1;
            } else {
                idFormaPago = jLFormaPago.getSelectedValue().getIdFormaPago();
            }

            String nombre = jTFFormaPago.getText();

            CFormaPago formaPago = new CFormaPago(idFormaPago, nombre, true);

            if (banderaAgregarEditar) {
                if (GBDFormaPago.ExisteNombreFormaPagoAgregar(formaPago)) {
                    JOptionPane.showMessageDialog(
                            this,
                            "¡Forma de Pago Repetida!",
                            "Advertencia - Nona Mafalda",
                            JOptionPane.WARNING_MESSAGE);
                    jTFFormaPago.requestFocus();
                    return;
                }
            } else {
                if (GBDFormaPago.ExisteNombreFormaPagoEditar(formaPago)) {
                    JOptionPane.showMessageDialog(
                            this,
                            "¡Forma de Pago Repetida!",
                            "Advertencia - Nona Mafalda",
                            JOptionPane.WARNING_MESSAGE);
                    jTFFormaPago.requestFocus();
                    return;
                }
            }


            boolean exito = false;

            if (idFormaPago == -1) {
                exito = GBDFormaPago.AgregarFormaPago(formaPago);
            } else {
                try {
                    exito = GBDFormaPago.ModificarFormaPago(formaPago);
                } catch (SQLException ex) {
                    Logger.getLogger(JPFormaPago.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
            if (exito) {
                JOptionPane.showMessageDialog(
                        this,
                        "La Forma de Pago se guardó Correctamente",
                        "Exito - Nona Mafalda",
                        JOptionPane.INFORMATION_MESSAGE);

                CargarLista();

                for (int i = 0; i < jLFormaPago.getModel().getSize(); i++) {
                    if (jLFormaPago.getModel().getElementAt(i).getFormaPago().equals(nombre)) {
                        jLFormaPago.setSelectedIndex(i);
                    }
                }
            }
        }
    }//GEN-LAST:event_jBGuardarActionPerformed

