    private void jBBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBorrarActionPerformed
        // TODO add your handling code here:
        if (jLFormaPago.getModel().getSize() > 0) {
            if (JOptionPane.showConfirmDialog(
                    this,
                    "Esta seguro?",
                    "Eliminando Tipo Producto - Nona Mafalda",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                // yes option
                int idFormaPago = jLFormaPago.getSelectedValue().getIdFormaPago();
                boolean exito = false;

                exito = GBDFormaPago.EliminarFormaPago(idFormaPago);

                if (exito) {
                    JOptionPane.showMessageDialog(
                            this,
                            "La Forma de Pago se eliminó correctamente.",
                            "Exito - Nona Mafalda",
                            JOptionPane.INFORMATION_MESSAGE);
                    CargarLista();
                } else {
                    JOptionPane.showConfirmDialog(
                            this,
                            "Intentelo Nuevamente.",
                            "Error al Eliminar el Tipo de Producto - Nona Mafalda",
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jBBorrarActionPerformed

